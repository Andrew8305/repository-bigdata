package org.platform.titan.modules.qq;

public class QQQun {

	/** QQ号 */
	private Integer qqNum = null;
	/** 昵称 */
	private String nickname = null;
	/** 年龄 */
	private Integer age = null;
	/** 性别 */
	private Integer gender = null;
	/** 授权 */
	private Integer auth = null;
	/** QQ群号 */
	private Integer qunNum = null;

	public Integer getQqNum() {
		return qqNum;
	}

	public void setQqNum(Integer qqNum) {
		this.qqNum = qqNum;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public Integer getGender() {
		return gender;
	}

	public void setGender(Integer gender) {
		this.gender = gender;
	}

	public Integer getAuth() {
		return auth;
	}

	public void setAuth(Integer auth) {
		this.auth = auth;
	}

	public Integer getQunNum() {
		return qunNum;
	}

	public void setQunNum(Integer qunNum) {
		this.qunNum = qunNum;
	}

}
